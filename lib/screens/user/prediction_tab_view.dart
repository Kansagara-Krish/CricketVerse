import 'package:flutter/material.dart';
import 'package:google_fonts/google_fonts.dart';
import '../../models/models.dart';
import '../../core/theme/app_theme.dart';
import '../../core/widgets/team_logo.dart';
import 'widgets/probability_gauge.dart';
import 'widgets/momentum_chart.dart';
import 'widgets/score_projection.dart';
import 'widgets/prediction_indicators.dart';

class PredictionTabView extends StatefulWidget {
  final CricketMatch match;

  const PredictionTabView({
    super.key,
    required this.match,
  });

  @override
  State<PredictionTabView> createState() => _PredictionTabViewState();
}

class _PredictionTabViewState extends State<PredictionTabView> {
  // Simulator State
  late int _wickets;
  late int _runsRequired;
  late int _oversRemaining;
  bool _isSimulatorActive = false;

  @override
  void initState() {
    super.initState();
    _resetToActuals();
  }

  void _resetToActuals() {
    setState(() {
      final m = widget.match;
      _wickets = m.isFirstInnings ? m.wicketsA : m.wicketsB;
      // Default runs needed to chase if target set, otherwise a mock scenario
      _runsRequired = m.target > 0 ? (m.target - m.runsB).clamp(1, 300) : 110;
      _oversRemaining = m.isFirstInnings 
          ? (20 - m.oversA.toInt()).clamp(1, 20) 
          : (20 - m.oversB.toInt()).clamp(1, 20);
      _isSimulatorActive = false;
    });
  }

  double _calculateSimulatedProbability() {
    // Standard cricket base probability (e.g. 50% split)
    double scoreFactor = 50.0;
    
    // Wickets penalty: Chasing team loses ~8.5% win probability per wicket lost
    double wicketPenalty = _wickets * 8.5;
    
    // Required Run Rate calculations
    double rrr = _oversRemaining > 0 ? _runsRequired / _oversRemaining : 36.0;
    
    // An RRR of 8 is considered neutral. Deviations scale probability.
    double rrrDiff = rrr - 8.0;
    double rrrImpact = rrrDiff * 6.5;

    // Simulated probability for the batting team
    double simulatedBattingProb = (scoreFactor + 20.0 - wicketPenalty - rrrImpact).clamp(2.0, 98.0);
    
    // Determine whether Team A or Team B is batting
    bool isTeamABatting = widget.match.battingTeamId == widget.match.teamA.id;
    if (isTeamABatting) {
      return simulatedBattingProb;
    } else {
      return 100.0 - simulatedBattingProb;
    }
  }

  @override
  Widget build(BuildContext context) {
    final simulatedWinProb = _calculateSimulatedProbability();

    return SingleChildScrollView(
      padding: const EdgeInsets.symmetric(horizontal: 16.0, vertical: 20.0),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Match Header Title
          _buildMatchHeaderCard(),
          const SizedBox(height: 20),

          // Win Probability gauge
          ProbabilityGauge(
            match: widget.match,
            winProbability: simulatedWinProb,
          ),
          const SizedBox(height: 20),

          // Interactive What-If Simulator Panel
          _buildSimulatorPanel(),
          const SizedBox(height: 20),

          // Score Projection Widget
          ScoreProjection(match: widget.match),
          const SizedBox(height: 20),

          // Momentum chart
          MomentumChart(match: widget.match),
          const SizedBox(height: 20),

          // Prediction Indicators
          const PredictionIndicators(),
          const SizedBox(height: 24),

          // Disclaimer
          Center(
            child: Text(
              'Predictions are generated by the CricketVerse AI Core based on real-time factors and history.',
              textAlign: TextAlign.center,
              style: GoogleFonts.plusJakartaSans(
                fontSize: 11,
                color: AppTheme.textMuted,
                height: 1.4,
              ),
            ),
          ),
          const SizedBox(height: 32),
        ],
      ),
    );
  }

  Widget _buildMatchHeaderCard() {
    return Center(
      child: Container(
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
        decoration: BoxDecoration(
          color: Colors.white,
          borderRadius: BorderRadius.circular(16),
          border: Border.all(color: AppTheme.bgSurface),
        ),
        child: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            TeamLogo(
              teamName: widget.match.teamA.name,
              shortName: widget.match.teamA.shortName,
              logoColorHex: widget.match.teamA.logoColorHex,
              size: 26,
            ),
            const SizedBox(width: 10),
            Text(
              widget.match.teamA.shortName,
              style: GoogleFonts.plusJakartaSans(fontSize: 14, fontWeight: FontWeight.w800),
            ),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 12.0),
              child: Text(
                'VS',
                style: GoogleFonts.plusJakartaSans(
                  fontSize: 11,
                  fontWeight: FontWeight.w900,
                  color: AppTheme.textMuted,
                ),
              ),
            ),
            Text(
              widget.match.teamB.shortName,
              style: GoogleFonts.plusJakartaSans(fontSize: 14, fontWeight: FontWeight.w800),
            ),
            const SizedBox(width: 10),
            TeamLogo(
              teamName: widget.match.teamB.name,
              shortName: widget.match.teamB.shortName,
              logoColorHex: widget.match.teamB.logoColorHex,
              size: 26,
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildSimulatorPanel() {
    return Container(
      width: double.infinity,
      padding: const EdgeInsets.all(20),
      decoration: BoxDecoration(
        color: Colors.white,
        borderRadius: BorderRadius.circular(24),
        border: Border.all(
          color: _isSimulatorActive ? AppTheme.primaryBlue.withValues(alpha: 0.4) : AppTheme.bgSurface,
          width: _isSimulatorActive ? 1.5 : 1.0,
        ),
        boxShadow: [
          BoxShadow(
            color: _isSimulatorActive 
                ? AppTheme.primaryBlue.withValues(alpha: 0.03) 
                : Colors.black.withValues(alpha: 0.01),
            blurRadius: 16,
            offset: const Offset(0, 8),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              Row(
                children: [
                  Icon(
                    Icons.tune_rounded,
                    color: _isSimulatorActive ? AppTheme.primaryBlue : AppTheme.textSecondary,
                    size: 20,
                  ),
                  const SizedBox(width: 8),
                  Text(
                    'What-If Scenario Simulator',
                    style: GoogleFonts.plusJakartaSans(
                      fontSize: 15,
                      fontWeight: FontWeight.w700,
                      color: AppTheme.textPrimary,
                    ),
                  ),
                ],
              ),
              if (_isSimulatorActive)
                TextButton(
                  onPressed: _resetToActuals,
                  style: TextButton.styleFrom(
                    padding: const EdgeInsets.symmetric(horizontal: 10, vertical: 4),
                    minimumSize: Size.zero,
                    tapTargetSize: MaterialTapTargetSize.shrinkWrap,
                  ),
                  child: Text(
                    'Reset to Live',
                    style: GoogleFonts.plusJakartaSans(
                      fontSize: 12,
                      fontWeight: FontWeight.w800,
                      color: AppTheme.accentRed,
                    ),
                  ),
                ),
            ],
          ),
          const SizedBox(height: 16),
          _buildSimulatorSlider(
            label: 'Wickets Lost',
            value: _wickets.toDouble(),
            min: 0,
            max: 10,
            divisions: 10,
            valueSuffix: '',
            color: AppTheme.accentRed,
            onChanged: (val) {
              setState(() {
                _wickets = val.round();
                _isSimulatorActive = true;
              });
            },
          ),
          const SizedBox(height: 12),
          _buildSimulatorSlider(
            label: 'Runs Required',
            value: _runsRequired.toDouble(),
            min: 5,
            max: 200,
            divisions: 39,
            valueSuffix: ' runs',
            color: AppTheme.primaryBlue,
            onChanged: (val) {
              setState(() {
                _runsRequired = val.round();
                _isSimulatorActive = true;
              });
            },
          ),
          const SizedBox(height: 12),
          _buildSimulatorSlider(
            label: 'Overs Remaining',
            value: _oversRemaining.toDouble(),
            min: 1,
            max: 20,
            divisions: 19,
            valueSuffix: ' overs',
            color: AppTheme.accentGold,
            onChanged: (val) {
              setState(() {
                _oversRemaining = val.round();
                _isSimulatorActive = true;
              });
            },
          ),
        ],
      ),
    );
  }

  Widget _buildSimulatorSlider({
    required String label,
    required double value,
    required double min,
    required double max,
    required int divisions,
    required String valueSuffix,
    required Color color,
    required ValueChanged<double> onChanged,
  }) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          mainAxisAlignment: MainAxisAlignment.spaceBetween,
          children: [
            Text(
              label,
              style: GoogleFonts.plusJakartaSans(
                fontSize: 12,
                fontWeight: FontWeight.w600,
                color: AppTheme.textSecondary,
              ),
            ),
            Text(
              '${value.toStringAsFixed(0)}$valueSuffix',
              style: GoogleFonts.plusJakartaSans(
                fontSize: 13,
                fontWeight: FontWeight.w800,
                color: color,
              ),
            ),
          ],
        ),
        SliderTheme(
          data: SliderThemeData(
            activeTrackColor: color,
            inactiveTrackColor: AppTheme.bgSurface,
            thumbColor: color,
            overlayColor: color.withValues(alpha: 0.12),
            valueIndicatorColor: color,
            trackHeight: 4,
            thumbShape: const RoundSliderThumbShape(enabledThumbRadius: 7),
            overlayShape: const RoundSliderOverlayShape(overlayRadius: 14),
          ),
          child: Slider(
            value: value,
            min: min,
            max: max,
            divisions: divisions,
            onChanged: onChanged,
          ),
        ),
      ],
    );
  }
}
